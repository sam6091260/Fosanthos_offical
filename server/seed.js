async function seed() {
  try {
    await mongoose.connect(process.env.MONGO_URI);
    console.log('✅ 已連接 MongoDB');

    // 清除舊資料
    await Post.deleteMany({});
    console.log('🗑️  已清除舊資料');

    // 插入新資料
    const result = await Post.insertMany(blogPosts);
    console.log(`✅ 成功新增 ${result.length} 篇文章`);

    // 顯示插入的文章
    result.forEach(post => {
      console.log(`   - [${post.category}] ${post.title}`);
    });

    await mongoose.disconnect();
    console.log('✅ 完成，已斷開連線');
  } catch (err) {
    console.error('❌ 錯誤:', err.message);
    process.exit(1);
  }
}

seed();
